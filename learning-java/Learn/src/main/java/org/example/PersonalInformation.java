package org.example;

public class PersonalInformation {
    private String name;
    private int options;

    public void setOptions(int options) {
        this.options = options;
    }

    public int getOptions() {
        return options;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {

        this.name = name;
    }
}
